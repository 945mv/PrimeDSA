#include<iostream>
using namespace std;
int main() {
    cout<<"Bitwise AND Operator --> " << (6&10) <<endl;
    cout<<endl;
    cout<<"Bitwise OR Operator --> " << (6|10) <<endl;
    cout<<endl;
    cout<<"Bitwise XOR Operator --> " << (6^10) <<endl;
    cout<<endl;
    cout<<"Bitwise Left Shift Operator --> " << (6<<10) <<endl;
    cout<<endl;
    cout<<"Bitwise Right Shift Operator --> " << (6>>10) <<endl;
    return 0;
}